import { ApiProperty } from '@nestjs/swagger';
import { IsISO8601, IsString, IsUUID } from 'class-validator';

export class DiscordCommunityEventPatchResponseDto {
  @ApiProperty({
    required: true,
    description: 'The uuid of the event (generated by the server)',
  })
  @IsUUID()
  @IsString()
  communityEventId: string;

  @ApiProperty({
    required: true,
    description: 'The title of the event',
  })
  @IsString()
  title: string;

  @ApiProperty({
    required: true,
    description: 'The description of the event',
  })
  @IsString()
  description: string;

  @ApiProperty({
    required: true,
    description: 'The username of the organizer',
  })
  @IsString()
  organizerUsername: string;

  @ApiProperty({
    required: true,
    description: 'The start date of the event',
  })
  @IsISO8601()
  startDate: string;

  @ApiProperty({
    required: true,
    description: 'The end date of the event',
  })
  @IsISO8601()
  endDate: string;
}
